package DSA_Questions.Leetcode;

import DSA_Questions.Leetcode.LinkedList.MergeTwoSortedLists;

public class ListNode {
    public int val;
    public ListNode next;
    ListNode() {}
    public ListNode(int val) {
        this.val = val;
    }
    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}